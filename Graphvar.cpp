//-------------------------------------
//   SettaZonaSpeciale()
//-------------------------------------
//  Funzione che controlla se il noto Target appartiene (come ZP)
//  a una macro Zona speciale (che raggruppa diverse zone primarie) e ne restituisce il valore 51...52
//
int SettaZonaSpeciale(int NumAgv, int ZonaSpeciale, int NodoTarget)
{
  int i,j;
  int AreaSpecialePerPercorsoAlternativo=0;
  int PercorsiSpeciali=0;

  PercorsiSpeciali= AGV[ NumAgv ]->PercorsiSpeciali;
  //--------------------------------------------------------------------
  // Verifica se il Nodo Target fa parte come Zp dell'area speciale
  //
  // Come in FATER spesso si avr l'esigenza di valutare 2 zone speciali
  // per ogni indice di percorso speciale:
  //
  // PercorsiSpeciali=1 Valuta zone speciale 101 oppure 201
  //                 =2 Valuta zone speciale 102 oppure 202
  //                 =3 Valuta zone speciale 103 oppure 203
  //                 ...
  //--------------------------------------------------------------------
  for(i=1; i<=20; i++){
     if( AreaSpeciale[i][0]==0 ) break;
     if((AreaSpeciale[i][0]==ZonaSpeciale+(PercorsiSpeciali*100))){
        for(j=1; j<=11; j++){
           if( AreaSpeciale[i][j]==0) break;
           if( N.n[NodoTarget]->zp==AreaSpeciale[i][j] ){
              AreaSpecialePerPercorsoAlternativo = AreaSpeciale[i][0];
              break;
           }
        }
     }
     if( AreaSpecialePerPercorsoAlternativo>0 ) break;
  }


  return AreaSpecialePerPercorsoAlternativo;
}

